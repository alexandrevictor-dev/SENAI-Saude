raio = float(input("Informe o valor do raio: "))
pi = 3.1415
comprimento = 2 * pi * raio
area = 4* pi * raio**2
volume = 4 / 3 * pi * raio**3
print("")
print("---------RESULTADOS---------")
print(f"Comprimento da esfera: {comprimento:.2f}")
print(f"Area da esfera: {area:.2f}")
print(f"Volume da esfera: {volume:.2f}")